package com.galaxy.common.util;

/**
 * This class contains the static utility methods and constants to be used in
 * the application.
 * 
 * @author Pradeep kumar
 *
 */
public class Utility {

	/**
	 * <p>
	 * This method prints the String message passed as an parameter.<br>
	 * The parameter must be string
	 * </p>
	 * 
	 * @param msg String
	 * @see #println(String)
	 */
	public static void print(String msg) {
		System.out.print(msg);
	}

	/**
	 * <p>
	 * This method prints the String message passed as an parameter with a new
	 * line.<br>
	 * The parameter must be string
	 * </p>
	 * 
	 * @param msg String
	 * @see #print(String)
	 */
	public static void println(String msg) {
		System.out.println(msg);
	}

}
